# Integer, int, tam sayı
# Python içerisinde küsüratsız sayıları işleme yöntemimizdir.
# Direkt olarak sayının kendisininin yazılması ile tamımlanır

ornek_sayi = 42
print(ornek_sayi)

print(ornek_sayi + 10)
print(ornek_sayi - 10)
print(ornek_sayi * 10)
print(ornek_sayi / 10)

# ornek_sayi = ornek_sayi + 2
ornek_sayi += 2

print(ornek_sayi)

a = ornek_sayi + 50
b = a * 20
c = b - 10
d = a + b + c
print(d)

print(ornek_sayi % 10)
